function cambiarIdiomaTecnologias(lang) {
    document.querySelector("#tecnologias h2").textContent = translations.tecnologias[lang].titulo;
    
    const columnas = document.querySelectorAll("#tecnologias .columna");
    
    columnas[0].querySelector("h3").textContent = translations.tecnologias[lang].lenguajes;
    columnas[1].querySelector("h3").textContent = translations.tecnologias[lang].herramientas;
    columnas[2].querySelector("h3").textContent = translations.tecnologias[lang].frameworks;
    columnas[3].querySelector("h3").textContent = translations.tecnologias[lang].basesDatos;

    // Actualizar los nombres de las tecnologías
    const tarjetas = document.querySelectorAll("#tecnologias .card p");
    
    translations.tecnologias[lang].listaLenguajes.forEach((nombre, index) => {
        tarjetas[index].textContent = nombre;
    });

    translations.tecnologias[lang].listaHerramientas.forEach((nombre, index) => {
        tarjetas[index + 6].textContent = nombre;
    });

    translations.tecnologias[lang].listaFrameworks.forEach((nombre, index) => {
        tarjetas[index + 8].textContent = nombre;
    });

    translations.tecnologias[lang].listaBasesDatos.forEach((nombre, index) => {
        tarjetas[index + 12].textContent = nombre;
    });
}

function cambiarIdiomaProyectos(lang) {
    console.log("Ejecutando cambiarIdiomaProyectos con idioma:", lang);

    // Cambiar el título de la sección
    document.querySelector("#proyectos .section-title").textContent = translations.proyectos[lang].titulo;

    // Seleccionar todas las tarjetas de proyectos
    const projectCards = document.querySelectorAll(".project-card");
    console.log("Número de project-card encontradas:", projectCards.length);

    if (projectCards.length === 0) {
        console.warn("No se encontraron project-card.");
        return;
    }

    // Obtener los datos traducidos
    const proyectos = [translations.proyectos[lang].proyecto1, translations.proyectos[lang].proyecto2];

    projectCards.forEach((card, index) => {
        if (proyectos[index]) {
            console.log(`Actualizando tarjeta ${index + 1}:`, proyectos[index].titulo);

            // Cambiar el título del proyecto
            const titulo = card.querySelector(".project-info h3");
            if (titulo) titulo.textContent = proyectos[index].titulo;

            // Cambiar la descripción del proyecto
            const descripcion = card.querySelector(".project-info p");
            if (descripcion) descripcion.textContent = proyectos[index].descripcion;

            // Cambiar las etiquetas (tags)
            const tags = card.querySelectorAll(".tags .tag");
            console.log("Número de etiquetas encontradas:", tags.length);
            tags.forEach((tag, tagIndex) => {
                if (proyectos[index].tags[tagIndex]) {
                    tag.textContent = proyectos[index].tags[tagIndex];
                }
            });

            // Cambiar el texto del botón de GitHub
            const githubBtn = card.querySelector(".project-footer a");
            if (githubBtn) githubBtn.textContent = proyectos[index].github;
        }
    });
}

// Función para cambiar el idioma en la sección "Educación"
function cambiarIdiomaEducacion(lang) {
    document.querySelector("#educacion h2").textContent = translations.educacion[lang].titulo;

    const tarjetas = document.querySelectorAll(".education-card");

    translations.educacion[lang].lista.forEach((edu, index) => {
        const titulo = tarjetas[index].querySelector("h3");
        const descripcion = tarjetas[index].querySelector("p");

        titulo.textContent = edu.titulo;
        descripcion.textContent = edu.descripcion;
    });
}
// Función para cambiar el idioma en la sección "Contacto"
function cambiarIdiomaContacto(lang) {
    console.log("Cambiando idioma en sección Contacto a:", lang);

    const contactTitle = document.querySelector(".contact-info h2");
    if (contactTitle) contactTitle.textContent = translations.contacto[lang].tituloInfo;

    const contactInfo = document.querySelectorAll(".contact-info p");
    if (contactInfo.length >= 3) {
        contactInfo[0].innerHTML = `<strong>${translations.contacto[lang].mail}:</strong> juanosegura@icloud.com`;
        contactInfo[1].innerHTML = `<strong>${translations.contacto[lang].telefono}:</strong> +54 9 11 5400-4515`;
        contactInfo[2].innerHTML = `<strong>${translations.contacto[lang].linkedin}:</strong> <a href="https://www.linkedin.com/in/juanigsegura/" target="_blank">Juan Ignacio Segura</a>`;
    } else {
        console.warn("No se encontraron suficientes elementos de contacto.");
    }

    const contactFormTitle = document.querySelector(".contact-form h2");
    if (contactFormTitle) contactFormTitle.textContent = translations.contacto[lang].tituloFormulario;

    const labels = {
        nombre: document.querySelector("label[for='nombre']"),
        apellido: document.querySelector("label[for='apellido']"),
        email: document.querySelector("label[for='email']"),
        mensaje: document.querySelector("label[for='mensaje']")
    };

    Object.keys(labels).forEach((key) => {
        if (labels[key]) labels[key].textContent = translations.contacto[lang][key];
    });

    const submitButton = document.querySelector(".form-submit button");
    if (submitButton) submitButton.textContent = translations.contacto[lang].enviar;
}

// Función general para cambiar el idioma
function cambiarIdioma() {
    const lang = document.documentElement.lang === "es" ? "en" : "es";
    document.documentElement.lang = lang;

    console.log("Cambiando idioma a:", lang);

    // Cambiar el idioma de la sección "Inicio"
    const inicioTitle = document.querySelector("#inicio h1");
    if (inicioTitle) inicioTitle.innerHTML = translations[lang].inicio;

    const inicioDesc = document.querySelector("#inicio p");
    if (inicioDesc) inicioDesc.textContent = translations[lang].descripcion;

    const cvButtons = document.querySelectorAll(".cv-buttons a");
    if (cvButtons.length >= 2) {
        cvButtons[0].textContent = translations[lang].descargarCvIngles;
        cvButtons[1].textContent = translations[lang].descargarCvEspanol;
    }

    // Llamar a las funciones para cambiar cada sección
    cambiarIdiomaSobreMi(lang);
    cambiarIdiomaTecnologias(lang);
    cambiarIdiomaProyectos(lang);
    cambiarIdiomaEducacion(lang);
    cambiarIdiomaContacto(lang);
}

// Evento de cambio de idioma en el botón
document.getElementById("language-switch").addEventListener("click", cambiarIdioma);
